let random = Math.floor(Math.random() * 10) % 6;
switch (random) {
    case 0:
        console.log(" ");
        break;
    case 1:
        console.log("bla");
        break;
    case 2:
        console.log("bla-bla");
        break;
    case 3:
        console.log("bla-bla-bla");
        break;
    case 4:
        console.log("bla-bla-bla-bla");
        break;
    case 5:
        console.log("bla-bla-bla-bla-bla");
} 